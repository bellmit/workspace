package org.jumao.bi.entites.charts;

public class CommonBean {

	private String typeOrId = "-1";

	private String name;
	
	private String value;


	public CommonBean() {
	}

	public CommonBean(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public CommonBean(String typeOrId, String name, String value) {
		this.typeOrId = typeOrId;
		this.name = name;
		this.value = value;
	}

	public String getTypeOrId() {
		return typeOrId;
	}

	public void setTypeOrId(String typeOrId) {
		this.typeOrId = typeOrId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
