package org.jumao.bi.entites.jrt.financing;

/**
 */
public class EnNetflowOverview {

    private Integer pv;
    private Integer uv;
    private Double bounce_rate;
    private Double avg_session_dur;
    private Integer users;
    private Integer new_users;
    private Integer sessions;


    public Integer getPv() {
        return pv;
    }

    public void setPv(Integer pv) {
        this.pv = pv;
    }

    public Integer getUv() {
        return uv;
    }

    public void setUv(Integer uv) {
        this.uv = uv;
    }

    public Double getBounce_rate() {
        return bounce_rate;
    }

    public void setBounce_rate(Double bounce_rate) {
        this.bounce_rate = bounce_rate;
    }

    public Double getAvg_session_dur() {
        return avg_session_dur;
    }

    public void setAvg_session_dur(Double avg_session_dur) {
        this.avg_session_dur = avg_session_dur;
    }

    public Integer getUsers() {
        return users;
    }

    public void setUsers(Integer users) {
        this.users = users;
    }

    public Integer getNew_users() {
        return new_users;
    }

    public void setNew_users(Integer new_users) {
        this.new_users = new_users;
    }

    public Integer getSessions() {
        return sessions;
    }

    public void setSessions(Integer sessions) {
        this.sessions = sessions;
    }
}
