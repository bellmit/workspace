package org.jumao.bi.entites.baidu;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * Function: 全世界地域访问分布bean
 * 
 * @author 1
 * @date 2017年7月17日 下午5:12:28
 * @version 
 * @see
 */
public class WorldVisitBean implements Serializable{

    private static final long serialVersionUID = -3924226318664760254L;
    private String country;
    private BigDecimal pv;
    private BigDecimal uv;
    private BigDecimal ip;
    
    /**
     * Creates a new instance of WorldVisitBean
     * 
     * @param country
     * @param pv
     * @param uv
     * @param ip
     */
    public WorldVisitBean(String country, BigDecimal pv, BigDecimal uv, BigDecimal ip) {
        this.country = country;
        this.pv = pv;
        this.uv = uv;
        this.ip = ip;
    }
    public String getCountry() {
        return country;
    }
    public void setCountry(String country) {
        this.country = country;
    }
    public BigDecimal getPv() {
        return pv;
    }
    public void setPv(BigDecimal pv) {
        this.pv = pv;
    }
    public BigDecimal getUv() {
        return uv;
    }
    public void setUv(BigDecimal uv) {
        this.uv = uv;
    }
    public BigDecimal getIp() {
        return ip;
    }
    public void setIp(BigDecimal ip) {
        this.ip = ip;
    }
    
    

}
