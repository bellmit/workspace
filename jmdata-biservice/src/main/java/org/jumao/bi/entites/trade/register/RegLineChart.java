package org.jumao.bi.entites.trade.register;


import java.util.ArrayList;
import java.util.List;

/**
 * 可表示折线图、柱状图、条形图
 */
public class RegLineChart {

    private String[] legendData = new String[0];
    private String title = new String();
    private String[] xAxisData = new String[0];
    private String[] yAxisData = new String[0];
    private String[] seriesData = new String[0];
    private List<String[]> seriesDataMulti = new ArrayList<String[]>();
    private String unit = new String();

    public String[] getLegendData() {
        return legendData;
    }

    public void setLegendData(String legendData) {
        this.legendData = new String[]{legendData};
//        this.legendData = legendData;
        this.title = legendData;
    }

    public void setLegendData(String[] legendData) {
        this.legendData = legendData;
    }

    public String[] getxAxisData() {
        return xAxisData;
    }

    public void setxAxisData(String[] xAxisData) {
        this.xAxisData = xAxisData;
    }

    public String[] getyAxisData() {
        return yAxisData;
    }

    public void setyAxisData(String[] yAxisData) {
        this.yAxisData = yAxisData;
    }

    public String[] getSeriesData() {
        return seriesData;
    }

    public void setSeriesData(String[] seriesData) {
        this.seriesData = seriesData;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public List<String[]> getSeriesDataMulti() {
        return seriesDataMulti;
    }

    public void setSeriesDataMulti(List<String[]> seriesDataMulti) {
        this.seriesDataMulti = seriesDataMulti;
    }
}
